### 1. print()

# Comillas dobles
print("Hello, World!")

# Comillas simples
print('Hello, World!')

# Comillas triples
print("""Hello, 
World!""")

# print() con argumentos
print("Hello", "World!")

# print() con argumentos y separador
print("Hello", "World!", sep=" ")

# print() con argumentos y separador y final
print("Hello", "World!", sep=" ", end="\n")

# print() con formato
print("Hello, {}!".format("World"))

